package one;


public class protection {
   int one = 2;
   public int two = 4;
   private int three = 9;
   protected int four = 21;

   public protection(){
       System.out.println("Base Constructor");
       System.out.println("one: " + one);
       System.out.println("two: " + two);
       System.out.println("three: " + three);
       System.out.println("four: " + four);
   }
}
